DEFAULT_FONT = "Yu Mincho"  # 日本語向け
ALT_FONT = "Arial"


def set_run_font(run, size_pt=10.5, bold=False, color=None):
    run.font.name = ALT_FONT
    run.font.size = Pt(size_pt)
    run.bold = bold
    if color:
        run.font.color.rgb = RGBColor(*color)
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.append(rFonts)
    rFonts.set(qn("w:eastAsia"), DEFAULT_FONT)
    rFonts.set(qn("w:ascii"), ALT_FONT)
    rFonts.set(qn("w:hAnsi"), ALT_FONT)


def set_cell_shading(cell, fill_color):
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), fill_color)
    tc_pr.append(shd)


def add_h1(doc, text):
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_run_font(r, size_pt=14, bold=True, color=(0x1F, 0x3A, 0x5F))
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(6)
    return p


def add_h2(doc, text):
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_run_font(r, size_pt=12, bold=True, color=(0x2E, 0x5C, 0x8A))
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(4)
    return p


def add_h3(doc, text):
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_run_font(r, size_pt=11, bold=True, color=(0x44, 0x44, 0x44))
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after = Pt(2)
    return p


def add_para(doc, text, bold=False, italic=False):
    p = doc.add_paragraph()
    r = p.add_run(text)
    set_run_font(r, bold=bold)
    r.italic = italic
    p.paragraph_format.space_after = Pt(4)
    return p


def add_bullet(doc, text):
    p = doc.add_paragraph(style="List Bullet")
    r = p.runs[0] if p.runs else p.add_run(text)
    if not p.runs:
        p.add_run(text)
        r = p.runs[0]
    else:
        r.text = text
    set_run_font(r)
    p.paragraph_format.space_after = Pt(2)
    return p


def add_table(doc, header, rows, col_widths_cm=None, header_fill="DCE6F1",
              first_col_bold=False):
    table = doc.add_table(rows=1 + len(rows), cols=len(header))
    table.style = "Table Grid"
    table.autofit = False
    if col_widths_cm:
        for i, w in enumerate(col_widths_cm):
            for cell in table.columns[i].cells:
                cell.width = Cm(w)
    # ヘッダ
    for i, h in enumerate(header):
        c = table.rows[0].cells[i]
        c.text = ""
        p = c.paragraphs[0]
        r = p.add_run(h)
        set_run_font(r, size_pt=9.5, bold=True)
        set_cell_shading(c, header_fill)
        c.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    # データ
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            c = table.rows[1 + ri].cells[ci]
            c.text = ""
            p = c.paragraphs[0]
            r = p.add_run(str(val))
            set_run_font(r, size_pt=9.5, bold=(first_col_bold and ci == 0))
            c.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    return table


