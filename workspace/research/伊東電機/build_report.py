def make_table(headers, rows, col_widths=None, header_shade='1F4E78'):
    n_cols = len(headers)
    table = doc.add_table(rows=1+len(rows), cols=n_cols)
    table.style = 'Light Grid Accent 1'
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        set_cell_jp(cell, h, bold=True, size=9)
        shade_cell(cell, header_shade)
        for para in cell.paragraphs:
            for run in para.runs:
                run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
    for r_idx, row in enumerate(rows):
        for c_idx, val in enumerate(row):
            set_cell_jp(table.rows[1+r_idx].cells[c_idx], val, bold=False, size=9)
    if col_widths:
        for r in table.rows:
            for c_idx, w in enumerate(col_widths):
                r.cells[c_idx].width = Cm(w)
    return table


