def _set_jp_font(run):
    rpr = run._element.get_or_add_rPr()
    rfonts_el = OxmlElement('w:rFonts')
    rfonts_el.set(qn('w:eastAsia'), 'Yu Gothic')
    rfonts_el.set(qn('w:ascii'), 'Arial')
    rfonts_el.set(qn('w:hAnsi'), 'Arial')
    rpr.append(rfonts_el)


def set_cell_jp(cell, text, bold=False, size=9):
    cell.text = ""
    p = cell.paragraphs[0]
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.font.name = 'Arial'
    _set_jp_font(run)
    if bold:
        run.bold = True


def shade_cell(cell, color_hex):
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), color_hex)
    tc_pr.append(shd)


