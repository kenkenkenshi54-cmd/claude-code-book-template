def add_table(headers, rows, col_widths=None, header_color='1F3A5F', shade_first_col=False, fontsize=9):
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    table.allow_autofit = False
    for j, h in enumerate(headers):
        cell = table.cell(0, j)
        cell.text = ''
        p = cell.paragraphs[0]
        p.paragraph_format.space_after = Pt(1)
        p.paragraph_format.space_before = Pt(1)
        run = p.add_run(h)
        run.bold = True
        run.font.size = Pt(fontsize)
        run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
        set_cell_shading(cell, header_color)
        cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    for i, row in enumerate(rows):
        for j, val in enumerate(row):
            cell = table.cell(i + 1, j)
            cell.text = ''
            p = cell.paragraphs[0]
            p.paragraph_format.space_after = Pt(1)
            p.paragraph_format.space_before = Pt(1)
            run = p.add_run(str(val))
            run.font.size = Pt(fontsize)
            if shade_first_col and j == 0:
                run.bold = True
                set_cell_shading(cell, 'EAEFF5')
            if i % 2 == 1 and not (shade_first_col and j == 0):
                set_cell_shading(cell, 'F4F6F9')
            cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
    if col_widths:
        for j, w in enumerate(col_widths):
            for i in range(len(rows) + 1):
                table.cell(i, j).width = Inches(w)
    return table


